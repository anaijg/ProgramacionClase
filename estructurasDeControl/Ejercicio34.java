package estructurasDeControl;

import java.util.Scanner;

public class Ejercicio34 {
    public static void main(String[] args) {
        /*
        34. Realizar un algoritmo que pida números (se pedirá por teclado la cantidad de
números a introducir). El programa debe informar de cuantos números introducidos
son mayores que 0, menores que 0 e iguales a 0.
         */

        // 1) pregunto cuántos números voy a introducir
        // ej 10

        Scanner sc = new Scanner(System.in);
        System.out.println("Números a introducir: ");
        int n = sc.nextInt();
        int número;
        int contadorPositivos = 0;
        int contadorNegativos = 0;
        int contadorCeros = 0;

        for (int i = 1; i <= n; i++) {
            System.out.println("Número:");
            número =sc.nextInt();
            if (número > 0) {
                contadorPositivos++; // también valdría: contadorPositivos = contadorPositivos + 1; o también contadorPositivos += contadorPositivos;
            } else if (número < 0 ) {
                contadorNegativos++;
            } else {
                contadorCeros++;
            }
        }
        System.out.println("Número de positivos: " + contadorPositivos);
        System.out.println("Número de negativos: " + contadorNegativos);
        System.out.println("Número de ceros: " + contadorCeros);

    }
}
